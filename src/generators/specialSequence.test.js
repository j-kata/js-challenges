const take = (length) => (xs) => Array.from({ length }, () => xs.next().value);
function* tail(xs) {
  xs.next();
  yield* xs;
}
function* derivative(xs) {
  xs.next();
  let k = 0;
  for (const x of xs)
    if (x === 1) k++;
    else yield k, (k = 0);
}
